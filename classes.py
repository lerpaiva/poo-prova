class ToDoList:
    def __init__(self):
        self.ToDo = []

    def adicionar_tarefa(self,descrição):
        self.descrição = descrição + descrição
        self.ToDo.append = [self.descrição]
        
    def excluir_tarefa(self, indice):
        for self.descrição in self.ToDo:
            del indice

    def listar_tarefas(self):
        for self.descrição in self.ToDo:
            return self.ToDo
        